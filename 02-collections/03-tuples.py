# create tuple from list
print("Create tuple from list")
my_list = ['value1', 'value2']
my_tuple = tuple(my_list)
print(my_tuple)

# create tuple from string
print("Create tuple from string")
my_tuple = tuple('value1')
print(my_tuple)

# create tuple from range
print("Create tuple from range")
my_tuple = tuple(range(10))
print(my_tuple)
